def create_image(data):
    """Create an image from a byte array."""
    image_array = np.array(data, dtype=np.uint8)
    image = Image.frombytes('1', (SCREEN_WIDTH, SCREEN_HEIGHT), image_array.tobytes())
    return image

def display_image(image):
    """Display an image on the OLED screen."""
    with canvas(device) as draw:
        draw.bitmap((0, 0), image, fill="white")

def scroll_text(text, duration=4, speed=10):
    """Scroll text horizontally across the OLED screen."""
    # Load a TTF font with a larger size
    font_path = "/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf"  # Adjust the path to your font file
    font_size = 16  # Adjust the font size as needed
    font = ImageFont.truetype(font_path, font_size)
    
    # Create a dummy image to get text size
    dummy_image = Image.new("1", (SCREEN_WIDTH, SCREEN_HEIGHT))
    draw = ImageDraw.Draw(dummy_image)
    text_width, text_height = draw.textsize(text, font=font)
    
    x = SCREEN_WIDTH
    start_time = time.time()
    
    while time.time() - start_time < duration:
        with canvas(device) as draw:
            draw.text((x, (SCREEN_HEIGHT - text_height) // 2), text, font=font, fill="white")
        x -= speed
        if x < -text_width:
            x = SCREEN_WIDTH
        time.sleep(0.03)  # Adjust the sleep time for smoother or faster scrolling

